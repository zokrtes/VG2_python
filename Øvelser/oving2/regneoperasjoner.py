#   lag to variabler for tall
#   konverter text variabel til tall (interger)
tall1_txt = input("Fuck deg din lille dritt, skriv inn ett tall: 10")
tall2_txt = input("Du er stygg ass, skriv inn ett tall: ")

#henter string valuen til tall1_txt og tall2_txt og gjør den om til en float value.ø
tall1 = float(tall1_txt)
tall2 = float(tall2_txt)

#   addisjon
print(tall1, " + ", tall2, " = ", tall1 + tall2)
#   subtraksjon
print(tall1, " - ", tall2, " = ", tall1 - tall2)
#   divisjon
print(tall1, " / ", tall2, " = ", tall1 / tall2)
#   multiplikasjon
print(tall1, " * ", tall2, " = ", tall1 * tall2)
